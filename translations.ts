export type LanguageCode = 'en' | 'th' | 'my';

export function getBrowserLang(): LanguageCode {
  const lang = navigator.language.split('-')[0];
  if (lang === 'th') return 'th';
  if (lang === 'my') return 'my';
  return 'en';
}
